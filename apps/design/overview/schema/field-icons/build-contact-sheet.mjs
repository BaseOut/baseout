// Renders all Airtable field-type icons into a single previewable page.
// Run with: node overview/schema/field-icons/build-contact-sheet.mjs
//
// Each .svg here is a bare <path> fragment on a 16x16 grid using
// currentColor. This script wraps each in an <svg viewBox> and lays them
// out as a labelled grid so the designer can eyeball the whole set.

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Filename (no ext) → human label. Mirrors README.md mapping.
const LABELS = {
  single_line_text: 'Single line text',
  long_text: 'Long text',
  rich_text: 'Rich text',
  number: 'Number',
  currency: 'Currency',
  percent: 'Percent',
  duration: 'Duration',
  rating: 'Rating',
  checkbox: 'Checkbox',
  single_select: 'Single select',
  multiple_select: 'Multiple select',
  date: 'Date',
  phone_number: 'Phone number',
  email: 'Email',
  url: 'URL',
  attachment: 'Attachment',
  barcode: 'Barcode',
  button: 'Button',
  link: 'Link to record',
  lookup: 'Lookup',
  rollup: 'Rollup',
  count: 'Count',
  formula: 'Formula',
  auto_number: 'Autonumber',
  collaborator: 'Collaborator',
  multiple_collaborator: 'Multiple collaborators',
  created_by: 'Created by',
  last_modified_by: 'Last modified by',
  created_time: 'Created time',
  last_modified_time: 'Last modified time',
  external_sync_souce: 'External sync source',
};

const files = readdirSync(__dirname)
  .filter((f) => f.endsWith('.svg'))
  .sort();

const cards = files
  .map((f) => {
    const key = f.replace(/\.svg$/, '');
    const path = readFileSync(join(__dirname, f), 'utf8').trim();
    const label = LABELS[key] ?? key;
    return `      <figure class="cell">
        <svg viewBox="0 0 16 16" width="24" height="24" aria-hidden="true">${path}</svg>
        <figcaption>${label}<code>${f}</code></figcaption>
      </figure>`;
  })
  .join('\n');

const html = `<!doctype html>
<html lang="en">
<head>
  <meta charset="utf-8" />
  <meta name="viewport" content="width=device-width, initial-scale=1" />
  <title>Airtable field-type icons — Baseout Schema page</title>
  <style>
    :root { color-scheme: light; }
    * { box-sizing: border-box; }
    body {
      margin: 0; padding: 32px;
      font: 14px/1.5 ui-sans-serif, system-ui, -apple-system, "Segoe UI", Roboto, sans-serif;
      color: #1e1e1e; background: #ffffff;
    }
    h1 { font-size: 20px; margin: 0 0 4px; }
    p.sub { color: #868e96; margin: 0 0 24px; max-width: 60ch; }
    .row-demo {
      display: flex; align-items: center; gap: 10px;
      padding: 10px 14px; border: 1px solid #e9ecef; border-radius: 8px;
      max-width: 360px; margin: 0 0 28px; color: #868e96;
    }
    .row-demo svg { color: #868e96; flex: none; }
    .row-demo .name { color: #1e1e1e; }
    .row-demo .type { margin-left: auto; font-size: 12px; }
    .grid {
      display: grid; gap: 10px;
      grid-template-columns: repeat(auto-fill, minmax(180px, 1fr));
    }
    .cell {
      display: flex; align-items: center; gap: 12px;
      margin: 0; padding: 12px 14px;
      border: 1px solid #e9ecef; border-radius: 8px; background: #f8f9fa;
    }
    .cell svg { color: #1e1e1e; flex: none; }
    figcaption { display: flex; flex-direction: column; line-height: 1.3; }
    figcaption code { color: #868e96; font-size: 11px; }
    footer { color: #868e96; margin-top: 28px; font-size: 12px; }
  </style>
</head>
<body>
  <h1>Airtable field-type icons</h1>
  <p class="sub">${files.length} icons, for the Schema page's Visualize side panel and Changelog rows. Each is a bare <code>&lt;path&gt;</code> on a 16×16 grid using <code>currentColor</code> — wrap in <code>&lt;svg viewBox="0 0 16 16"&gt;</code> and tint via CSS <code>color</code>.</p>

  <div class="row-demo">
    <svg viewBox="0 0 16 16" width="18" height="18" aria-hidden="true">${readFileSync(join(__dirname, 'single_select.svg'), 'utf8').trim()}</svg>
    <span class="name">Stage</span>
    <span class="type">single select</span>
  </div>

  <div class="grid">
${cards}
  </div>

  <footer>Generated by build-contact-sheet.mjs — re-run after adding or renaming icons.</footer>
</body>
</html>
`;

writeFileSync(join(__dirname, 'contact-sheet.html'), html);
console.log(`wrote contact-sheet.html (${files.length} icons)`);
